/// The public wildcard subject: `user:*`. Assignable only where the model
/// declares `[user:*]`.
pub struct FgaWildcard<T: FgaType>(PhantomData<T>);

impl<T: FgaType> fmt::Debug for FgaWildcard<T> {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "FgaWildcard(\"{}:*\")", T::NAME)
    }
}
impl<T: FgaType> Clone for FgaWildcard<T> {
    fn clone(&self) -> Self {
        *self
    }
}
impl<T: FgaType> Copy for FgaWildcard<T> {}
impl<T: FgaType> PartialEq for FgaWildcard<T> {
    fn eq(&self, _: &Self) -> bool {
        true
    }
}
impl<T: FgaType> Eq for FgaWildcard<T> {}

impl<T: FgaType> FgaWildcard<T> {
    pub const fn new() -> Self {
        FgaWildcard(PhantomData)
    }
}

impl<T: FgaType> Default for FgaWildcard<T> {
    fn default() -> Self {
        Self::new()
    }
}

impl<T: FgaType> fmt::Display for FgaWildcard<T> {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "{}:*", T::NAME)
    }
}

/// Marker pairing a wildcard subject with its type in
/// [`DirectlyAssignable`] bounds (`[user:*]` ⇒
/// `DirectlyAssignable<WildcardOf<user::Ty>>`).
pub struct WildcardOf<T: FgaType>(PhantomData<T>);

/// Anything that can be the subject of a relationship tuple: a typed object
/// (`user:alice`), a userset (`team:eng#member`), or a wildcard (`user:*`).
pub trait FgaSubject {
    /// The marker checked against [`DirectlyAssignable`]: the type marker
    /// itself for objects, `(Ty, RelMarker)` for usersets,
    /// [`WildcardOf<Ty>`] for wildcards.
    type Marker;

    /// The wire form (`user:alice`, `team:eng#member`, `user:*`).
    fn subject_str(&self) -> &str;
}

impl<T: FgaType> FgaSubject for FgaObject<T> {
    type Marker = T;

    fn subject_str(&self) -> &str {
        self.as_str()
    }
}

impl<T: FgaType, R> FgaSubject for FgaUserset<T, R> {
    type Marker = (T, R);

    fn subject_str(&self) -> &str {
        self.as_str()
    }
}

/// `FgaWildcard` renders lazily, so it keeps an owned copy per call site.
impl<T: FgaType> FgaSubject for FgaWildcard<T> {
    type Marker = WildcardOf<T>;

    fn subject_str(&self) -> &str {
        // `T::NAME:*` is 'static-derivable but not const-concatenable on
        // stable; leak once per type via OnceLock.
        static_wildcard::<T>()
    }
}

fn static_wildcard<T: FgaType>() -> &'static str {
    use std::collections::HashMap;
    use std::sync::{Mutex, OnceLock};
    static CACHE: OnceLock<Mutex<HashMap<&'static str, &'static str>>> = OnceLock::new();
    let cache = CACHE.get_or_init(|| Mutex::new(HashMap::new()));
    let mut map = cache.lock().expect("wildcard cache poisoned");
    map.entry(T::NAME)
        .or_insert_with(|| Box::leak(format!("{}:*", T::NAME).into_boxed_str()))
}

/// Implemented (by `model!`-generated code) on a relation marker for every
/// subject marker in the relation's `directly_related_user_types`. The typed
/// write API bounds `grant`/`revoke` on it: assigning a subject type the
/// model does not allow fails to compile.
pub trait DirectlyAssignable<SubjectMarker> {}
